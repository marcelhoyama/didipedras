

<h2>O que é muro de arrimo? Entenda sua importância e como deve ser construído</h2>
<b>3 DE DEZEMBRO DE 2014</b>

O muro de arrimo ou muro de contenção é um tipo específico de muro que serve para suportar a terra além de isolar o terreno.

Ele é uma solução de segurança para terrenos em declive e ou com inclinação que receberão cortes para se tornarem planos. Após o corte, surgirão as áreas de acomodação, o espaço entre o perfil original do lote e a área que se tornou plana.


O muro de arrimo serve para “segurar” a terra da parte que for mais alta entre os dois lados da acomodação, isto é, estabilizando a pressão e contendo a terra com risco de desmoronamento para que ela não ceda.

Assim, o sistema deve ser bem planejado, calculado e executado para impedir deslizamentos que possam gerar desastres mais tarde. Os muros irão segurar uma quantidade significativa de terra que é pesada e fica ainda mais quando chove.

<h3>Materiais</h3>

Os muros de arrimo podem ser construídos com blocos de concreto ou cerâmicos e com ou sem estrutura metálica.

Há a opção ainda de se construir com pedras, com solo-cimento (mistura de terra e cimento) ou de gabiões de pedra. Saiba o que é gabião e se inspire com 12 projetos de arquitetura e construção.


A forma mais barata de criar um muro de arrimo é construí-lo com blocos e apenas revestir posteriormente com algum material. Os muros de pedra são boas opções já que o peso da pedra é suficiente para contar a carga da terra, mas pode sair caro.

O muro ainda pode ser feito de maneiras diferentes: inclinado, em patamar ou aliado à vegetação.

<h3>Cuidados com a água</h3>
Quando chove, a água encharca a terra e a faz ganhar mais peso. Isso traz o risco do solo de um terreno declinado desmoronar e atingir o que se encontra mais abaixo.

Como já dito, o muro de arrimo surge para trazer segurança e impedir que esse tipo de desastre ocorra. Mas para que o sistema funcione perfeitamente e não deixe a desejar em momentos vitais como esse, são necessários alguns cuidados.



O principal cuidado a se tomar é prévio à construção do muro: o cálculo de sua estrutura deve contemplar a carga adicional proporcionada pela água, como uma espécie de margem de segurança.

Outro cuidado é tentar tirar o máximo de água da parte de trás do muro para aliviar o peso. Isso pode ser feito a partir de um sistema de drenagem: drenos que levam a água para as laterais do muro, podendo ainda atravessá-lo e estarem na sua parte frontal. A melhor solução deve ser pensada pelo engenheiro ou arquiteto.

Um fator importante é haver uma camada de brita entre a terra e os drenos para evitar que a tração neutra da água transporte pequenas partículas de terra para os drenos, que fará com que eles fiquem entupidos com o tempo e percam sua função.

Impermeabilizar a parte de trás do muro é um cuidado fundamental, principalmente se a frente estiver dentro da casa. A impermeabilidade fará com que a água só atravesse o muro pelos drenos, mantendo o outro lado seco.


Um método alternativo para impedir que a água não atravesse o muro de arrimo, porém de maior custo, é criar uma parede dupla – o muro de arrimo impermeabilizado e uma parede comum a sua frente, separados por um espaço de ar.

Por fim, é essencial que a obra do muro de arrimo seja feita por ótimos profissionais, desde o planejamento à mão de obra na execução. O acompanhamento técnico é imprescindível.

